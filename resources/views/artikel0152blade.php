<h1 class="mt-2">Kelebihan Framework</h1>
<p class="left">1. Lebih cepat dan efisien
Jika Anda mengerjakan proyek besar, maka penggunaan framework dapat membantu mempercepat proses pengembangan. Pada umumnya, framework memiliki beragam fungsi dan plugin yang bisa Anda manfaatkan. Dengan kerangka kerja ini, maka proses pengembangan proyek jauh lebih cepat daripada Anda harus menulis kode dari awal. Selain itu, Anda juga tidak perlu menulis berulang-ulang untuk kode yang bersifat repetitif.<p>
<p class="left">2. Menghemat biaya
Sebagian besar kerangka populer bersifat open source dan gratis untuk digunakan. Biaya yang harus dikeluarkan oleh client Anda juga akan menjadi lebih kecil karena proses pengerjaan yang lebih simple dan lebih cepat.<p>
<p class="left">3. Memperhatikan faktor keamanan
Framework populer telah banyak digunakan oleh banyak pengembang, dan kemungkinan adanya masalah keamanan atau pun bug telah diperbaiki. Selain itu, framework biasanya juga memiliki komunitas besar yang dapat berperan sebagai penguji jangka panjang. Setiap kali pengguna menemukan celah keamanan, maka mereka dapat memberi tahu tim untuk segera memperbaikinya.<p>
<h1 class="mt-2">Kekurangan Framework</h1>
<p class="left">1. Kurangnya pemahaman bahasa pemrograman
Jika Anda bekerja menggunakan kerangka kerja dan hanya mengetahui sedikit tentang bahasa pemrograman yang digunakan dibaliknya, maka Anda hanya mempelajari mengenai kerangka tersebut. Sehingga pemahaman mengenai  bahasa pemrograman menjadi tidak berkembang.<p>
<p class="left">2. Memiliki batasan
Dalam penggunaannya, kerangka ini juga memiliki beberapa batasan yang tidak dapat Anda modifikasi. Sehingga Anda harus bekerja sesuai dengan standar yang digunakan di dalamnya. Oleh karena itu, ketika mengembangkan sebuah aplikasi, Anda harus menggunakan kerangka kerja yang sesuai dengan kebutuhan Anda.<p>
